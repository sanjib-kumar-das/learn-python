a = 10
b = 3

# Arithmetic Operators
print("num1 + num2 is: ", a+b)  #addition
print("num1 - num2 is: ", a-b)  #subtraction
print("num1 * num2 is: ", a*b)  #multiplication
print("num1 / num2 is: ", a/b)  #division

print("num1 ** num2 is: ", a**b)  #a^b
print("num1 // num2 is: ", a//b)  #floor(a/b)
print("num1 % num2 is: ", a%b)  #(a mod b)

